public class _12_minus100to25 {
    public static void main(String[] args) {
        // 문제 : -100부터 25까지 출력해주세요.
        int i = -100;

        while (i <= 25) {
            System.out.println(i);
            i++;
        }
    }
/*
    출력
    -100
    -99
    -98
    -97
    -96
    -95
    ...
    25
    */
}